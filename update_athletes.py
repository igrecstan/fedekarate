def generate_update_queries(updates, batch_size=150):
    """
    Génère des requêtes UPDATE par lots pour la table athletes
    """
    queries = []
    
    for i in range(0, len(updates), batch_size):
        batch = updates[i:i+batch_size]
        
        case_when = '\n    '.join([f"WHEN {id_val} THEN '{date_val}'" for id_val, date_val in batch])
        ids_list = ','.join([str(id_val) for id_val, _ in batch])
        
        # ⚠️ CORRECTION : colonne ID = 'id_ath'
        query = f"""UPDATE `athletes` 
SET `created_at` = CASE `id_ath`
    {case_when}
END
WHERE `id_ath` IN ({ids_list});"""
        
        queries.append(query)
    
    return queries

